package pl.edu.pw.elka.home.sjablon1.model;
 
/**
 * Abstract class represents Bomberman object. In game we have two types
 * or Bomberman object: Player and Enemy.
 * @author Szymon Jablonski
 * @version 1.0
 */
public abstract class Bomberman
{
	/** Bomberman bomb Number */
	protected int bombNumber;
	/** Bomberman speed Level */
	protected int speedLevel;
	/** Bomberman bombs power Level */
	protected int explosionLevel;
}
